import numpy as np

# Crear un array unidimensional con números del 1 al 5
array_unidimensional = np.array([1, 2, 3, 4, 5])

# Imprimir el array
print("Array unidimensional:", array_unidimensional)

# Acceder a elementos individuales por índice
primer_elemento = array_unidimensional[0]
segundo_elemento = array_unidimensional[1]
tercer_elemento = array_unidimensional[2]

# Imprimir elementos individuales
print("\nPrimer elemento:", primer_elemento)
print("Segundo elemento:", segundo_elemento)
print("Tercer elemento:", tercer_elemento)
