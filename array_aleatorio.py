import numpy as np

# Array unidimensional de 5 elementos
array_unidimensional = np.random.rand(5)
print("Array unidimensional:")
print(array_unidimensional)

# Array bidimensional de forma (3, 4)
array_bidimensional = np.random.rand(3, 4)
print("\nArray bidimensional:")
print(array_bidimensional)

# Array tridimensional de forma (2, 3, 2)
array_tridimensional = np.random.rand(2, 3, 2)
print("\nArray tridimensional:")
print(array_tridimensional)
